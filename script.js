document.getElementById('personality').addEventListener('change', updateStyle);
document.getElementById('occupation').addEventListener('change', updateStyle);
document.getElementById('solo').addEventListener('click', () => showRecommendation('個人獨享'));
document.getElementById('group').addEventListener('click', () => showRecommendation('團體共享'));

function updateStyle() {
    const personality = document.getElementById('personality').value;
    const container = document.querySelector('.container');
    const result = document.getElementById('result');
    container.classList.remove('e-person', 'i-person');
    result.classList.remove('e-result', 'i-result');
    if (personality === 'E') {
        container.classList.add('e-person');
        result.classList.add('e-result');
    } else {
        container.classList.add('i-person');
        result.classList.add('i-result');
    }
}

function showRecommendation(scenario) {
    const personality = document.getElementById('personality').value;
    const occupation = document.getElementById('occupation').value;
    const recommendation = recommendations[occupation]?.[personality]?.[scenario];
    const resultText = document.getElementById('result-text');
    const resultImage = document.getElementById('result-image');
    
    console.log('Recommendation:', recommendation); // 調試日誌
    if (recommendation) {
        resultText.innerText = recommendation.text;
        resultImage.src = recommendation.image; // 顯示餐點圖片
        resultImage.style.display = 'block';
    } else {
        resultText.innerText = '無對應餐點推薦';
        resultImage.style.display = 'none';
    }
}